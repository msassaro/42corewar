#include "asm.h"

/*
** There is a description of the t_op structure:
** .
** char		*name				the name of the operation
** int		nb_param			nb of params accepted by the op
** int		type_param[4]		nb of parans accepted by the op
** int		opcode				the corresponding nb of the op
** int		nb_cycles			nb of cycles
** char		*description		useless
** int		encoding			if there is encodage
** int		idx					The op accept indexes
*/

